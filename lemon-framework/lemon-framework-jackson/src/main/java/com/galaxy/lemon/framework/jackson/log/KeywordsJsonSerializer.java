package com.galaxy.lemon.framework.jackson.log;

import com.galaxy.lemon.common.LemonConstants;
import com.galaxy.lemon.common.log.Keywords;
import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;

import java.io.IOException;

/**
 *
 * @author <mailto:eleven.hm@vip.163.com">eleven</a>
 * @see
 * @since 1.0.0
 */

public class KeywordsJsonSerializer extends JsonSerializer<Keywords> {

    @Override
    public void serialize(Keywords value, JsonGenerator gen, SerializerProvider serializers) throws IOException, JsonProcessingException {
        gen.writeString(null == value ? LemonConstants.EMPTY_STRING : value.toString());
    }
}
